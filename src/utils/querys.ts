export const feedQuery = (category = "", searchTerm = "") =>
  `*[_type == 'pin'
  ${category ? ` && category match '${category}*'` : ""}
  ${
    searchTerm
      ? ` && title match '${searchTerm}*' ||
  category match '${searchTerm}*' ||
  about match '${searchTerm}*'`
      : ""
  }
] | order(_createAt desc) {
  image {
    asset -> {
      url
    }
  },
  _id,
  title,
  destination,
  postedBy -> {
    _id,
    userName,
    image
  },
  save[] {
    _key,
    postedBy -> {
      _id,
      userName,
      image
    }
  }
}
`

